#include<iostream>
using namespace std;

int main()
{
    int t;
    cin >> t;
    for (int tcase = 1; tcase <= t; tcase++) {
        int a, b;
        cin >> a >> b;
        cout << "Case " << tcase << ": " << a + b << '\n';
    }

    return 0;
}

